#ifndef MATH_BASIC_MATH_H
#define MATH_BASIC_MATH_H

////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Simple math utilities.
////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////
// Namespace(s)
////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Math
{
namespace Basic_Math
{
    
////////////////////////////////////////////////////////////////////////////////////////////////////
/// @brief Squares a templated value.
///
/// @param[in] val The input value.
///
/// @return The squared value.
////////////////////////////////////////////////////////////////////////////////////////////////////
template <typename T> T squared(T val)
{
    return val * val;
}

} // namespace Basic_Math
} // namespace Math

#endif // MATH_BASIC_MATH_H
